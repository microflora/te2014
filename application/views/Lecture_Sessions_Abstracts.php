<h1><span id="lblPresentation">Lecture Sessions Abstracts</span></h1>

<h3>Session ID: BI100</h3>
<h3>SAP Business Objects BI Solutions Overview and Roadmap</h3>
<p>This session provides a good starting point for anyone looking to
better understand the complete suite of the SAP BusinessObjects Business
Intelligence (SAP BusinessObjects BI) solutions and how it fits within
an SAP landscape. In this session you will learn the main components of
the SAP BusinessObjects BI solutions, how SAP BusinessObjects BI
solutions integrate with SAP NetWeaver Business Warehouse (SAP NetWeaver
BW) and SAP ERP (ECC), what's new in the latest release of SAP
BusinessObjects BI, and what the big product enhancements planned for
SAP NetWeaver BW in 2014 are. Walk away from this session better
equipped to understand which specific SAP business intelligence sessions
you should attend based on this overview.</p>


<p><strong>Prerequisites:</strong><br />
None</p>
<hr width="90%" />

<h3>Session ID: ALM130</h3>
<h3>SAP Architecture, The Way Forward</h3>
<p>By introducing on-demand and on-device solutions, SAP broadens its
portfolio but raises interesting questions at the same time about how to
integrate these offerings into existing landscapes and how to handle
end-to-end processes across SAP and non-SAP solutions. The key
requirement for this is that our pieces fit together. Therefore SAP
defines architecture standards for interoperability which are essential
to make that happen. Through reference architecture and concrete
examples this lecture describes SAP's way forward to an open IT
landscape.</p>
<p><strong>Prerequisites:</strong><br />
None</p>
