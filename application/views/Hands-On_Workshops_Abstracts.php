<h1><span id="lblPresentation">Hands-On Workshops Abstracts</span></h1>

<h3>Session ID: TP260</h3>
<h3>SAP Technology: Putting it all Together</h3>
<p>SAP provides a comprehensive technology offering, including SAP
NetWeaver and SAP BusinessObjects solutions. In this workshop, you will
discover how to enhance your business applications and business
processes by using SAP's outstanding technology. See how you can drive
innovation in your company for information discovery and delivery,
Enterprise Information Management and process-centric Composite
Applications. Our workshop comprises ABAP Web Dynpro including Flash
Islands, SAP BusinessObjects Xcelsius accessing SAP NetWeaver Business
Warehouse, SAP NetWeaver Master Data Management, SAP NetWeaver Business
Process Management, and SAP BusinessObjects Explorer.</p>
<p><strong>Prerequisites:</strong><br />

None</p>
<hr width="90%" />

<h3>Session ID: BI263</h3>
<h3>Learn and Practice the New Multidimensional Semantic Layer for SAP
NetWeaver BW and OLAP Sources</h3>
<p>Come to learn and practice the new multidimensional Semantic Layer
for OLAP sources. Query, Report, Analyze and Dashboard on SAP Business
Explorer queries without additional administration thanks to the
Semantic Layer. Enrich your OLAP sources including SAP NetWeaver
Business Warehouse (SAP NetWeaver BW) though multidimensional universes
and use them for reporting, ad hoc, and dashboards. This workshop will
also cover a presentation and demonstration of the new multidimensional
Semantic Layer. Attendees will learn Semantic Layer concepts (universes)
and OLAP concepts.</p>

<p><strong>Prerequisites:</strong><br />
None</p>
